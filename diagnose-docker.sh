if [ -f "config.yaml" ]; then
    echo "‚úÖ config.yaml exists in current directory"
    ls -lh config.yaml
    echo ""
    echo "KEA URL configured:"
    grep "control_agent_url" config.yaml || echo "‚ö†Ô∏è  control_agent_url not found"
else
    echo "‚ùå config.yaml NOT FOUND in current directory!"
    echo "   Create one or cd to the directory containing config.yaml"
